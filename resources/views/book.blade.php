            <h3 class="card-title">Полиэтилентерефталат</h3>
            <div class="card-popup">PET(E) или ПЭТ означает полиэтилентерефталат. Чаще всего это бутылки с выпуклой точкой на дне, в которых продаются вода, газировка, молоко, подсолнечное масло. Также из ПЭТа часто делают прозрачные флаконы для шампу-ней, одноразовые контейнеры для еды. Можно сдать на переработку.</div>
        </div>
        <div class="card">
            <img src="img/02.svg" alt="Изображение 2" class="card-image">
            <h3 class="card-title">Полиэтилен высокой плотности</h3>
            <div class="card-popup">PEHD (HDPE) или ПНД — это полиэтилен низкого давления: канистры, крышки для бутылок, флаконы из-под косметики и быто-вой химии. Сюда можно отнести упаковки от шампуней, гелей для душа, моющих средств. Упаковка из такого пластика перерабатываемая</div>
        </div>
        <div class="card">
            <img src="img/03.svg" alt="Изображение 3" class="card-image">
            <h3 class="card-title">Поливинилхлорид</h3>
            <div class="card-popup">PVC или ПВХ — поливинилхлорид. Этот знак можно увидеть на окон-ных рамах, блистерах, упаковках из-под таблеток, а также тортов и творога, термоусадочной плёнке, флаконах для косметики, игрушках. Данную практически невозможно сдать на переработку, но некоторые заводы все-таки перерабатывают данный материал.</div>
        </div>
        <div class="card">
            <img src="img/04.svg" alt="Изображение 4" class="card-image">
            <h3 class="card-title">Полиэтилен низкой плотности</h3>
            <div class="card-popup">PELD (LDPE) или ПВД — это полиэтилен высокого давления и низкой плотности. К такой категории пластика можно отнести пакеты, бутылки от моющих средств и пленку. Материал можно сдать на переработку, но место переработки придется поискать</div>
        </div>
        <div class="card">
            <img src="img/05.svg" alt="Изображение 4" class="card-image">
            <h3 class="card-title">Полипропилен</h3>
            <div class="card-popup">PP или ПП обычно ставят на полипропилене. Это крышки для бутылок, пластиковые вёдра и ве-дёрки, стаканчики для йогурта, упаковка из под линз, шуршащая пластиковая упаковка. На этой продукции ставится цифра 5 в тре-угольнике, что означает возмож-ность дальнейшей переработки.</div>
        </div>
        <div class="card">
            <img src="img/06.svg" alt="Изображение 4" class="card-image">
            <h3 class="card-title">Полистирол</h3>
            <div class="card-popup">PS — это полистирол. Он бывает обычный и вспененный. Из вспененного делают пенопласт, контейнеры для яиц, лоточки для мяса и фасовки; из обычного — стаканчики для йогурта, упаковку для компакт-дисков, всю однора-зовую посуду. На всей этой продук-ции ставится цифра 6 в треуголь-нике. Упаковку можно сдать на переработку, но лучше избегать её использования — это вредно.</div>
        </div>
        <div class="card">
            <img src="img/07.svg" alt="Изображение 4" class="card-image">
            <h3 class="card-title">Полистирол</h3>
            <div class="card-popup">O (other — другое). Смесь различного пластика или поли-мера, не указанная выше. Сюда входят такие упаковки, как: тюбики зубной пасты, бутылки от кулера, корма для животных, бутылочки из поликарбоната, упаковка от за-мороженных продуктов. В насто-ящее время такой вид упаковки не подлежит дальнейшей пере-работке, о чем свидетельствует цифра 7 в треугольнике.</div>
        </div>
        <div class="card">
            <img src="img/09.svg" alt="Изображение 4" class="card-image">
            <h3 class="card-title">Полистирол</h3>
            <div class="card-popup">Широко используемый на сегодня АБС-пластик. Из него изготавливают корпуса мониторов, телевизоров и электроинструментов; кофеварки, сотовые телефоны, компьютерный пластик; музыкальные инструменты и канцелярские принадлежности; распечатанные на 3D-принтере компоненты, которые не являются биопластиками, такими как PLA. Можно повторно переработать.</div>
        </div>
    </div>
